package com.company;

public class Staff {
    private String name;
    private int id;
    private String level;



    public Staff(String name, int id, String level) {
        this.name = name;
        this.id = id;
        this.level = level;



    }

    public String getName() {

        return name;
    }

    public int getId() {

        return id;
    }

    public String getLevel() {

        return level;
    }


}
